BEGIN TRANSACTION;

DROP TABLE IF EXISTS INCLUDES;
DROP TABLE IF EXISTS COMPOSES;
DROP TABLE IF EXISTS CONTRACT_S;
DROP TABLE IF EXISTS RECORDING;
DROP TABLE IF EXISTS SALES;
DROP TABLE IF EXISTS SONG;
DROP TABLE IF EXISTS CREATOR;
DROP TABLE IF EXISTS PRODUCER;
DROP TABLE IF EXISTS STUDIO;
DROP TABLE IF EXISTS ALBUM;
CREATE TABLE IF NOT EXISTS "ALBUM" (
	"album_id"	INTEGER,
	"album_title"	varchar(30),
	"release_date"	DATE,
	PRIMARY KEY("album_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "STUDIO" (
	"studio_id"	INTEGER,
	"studio_name"	varchar(20),
	"street"	varchar(15),
	"streetnum"	varchar(3),
	"city"	varchar(15),
	"postcode"	varchar(5),
	"phone"	varchar(13),
	PRIMARY KEY("studio_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "PRODUCER" (
	"producer_id"	INTEGER,
	"firstname"	varchar(20),
	"lastname"	varchar(20),
	"phone"	varchar(13),
	"streetname"	varchar(15),
	"streetnum"	varchar(3),
	"city"	varchar(15),
	"postcode"	varchar(5),
	PRIMARY KEY("producer_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "CREATOR" (
	"creator_id"	INTEGER,
	"street"	varchar(15),
	"streetnum"	varchar(3),
	"city"	varchar(15),
	"postcode"	varchar(5),
	"firstname"	varchar(20),
	"lastname"	varchar(20),
	"artist_name"	varchar(30),
	"artist_role"	varchar(15),
	"phone"	varchar(13),
	PRIMARY KEY("creator_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "SONG" (
	"song_id"	INTEGER,
	"duration"	TIME,
	"song_title"	varchar(30),
	"category"	varchar(15),
	"release_date"	DATE,
	"studio_id"	INTEGER,
	"producer_id"	INTEGER,
	FOREIGN KEY("producer_id") REFERENCES "PRODUCER"("producer_id") ON UPDATE CASCADE ON DELETE SET NULL,
	FOREIGN KEY("studio_id") REFERENCES "STUDIO"("studio_id") ON UPDATE CASCADE ON DELETE SET NULL,
	PRIMARY KEY("song_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "INCLUDES" (
	"song_id"	INTEGER,
	"album_id"	INTEGER,
	FOREIGN KEY("song_id") REFERENCES "SONG"("song_id") ON UPDATE CASCADE ON DELETE SET NULL,
	FOREIGN KEY("album_id") REFERENCES "ALBUM"("album_id") ON UPDATE CASCADE ON DELETE SET NULL
);
CREATE TABLE IF NOT EXISTS "COMPOSES" (
	"creator_id"	INTEGER,
	"song_id"	INTEGER,
	FOREIGN KEY("creator_id") REFERENCES "CREATOR"("creator_id") ON UPDATE CASCADE ON DELETE SET NULL,
	FOREIGN KEY("song_id") REFERENCES "SONG"("song_id") ON UPDATE CASCADE ON DELETE SET NULL
);
CREATE TABLE IF NOT EXISTS "CONTRACT_S" (
	"contract_id"	INTEGER,
	"date_start"	DATE,
	"date_end"	DATE,
	"creator_id"	INTEGER,
	FOREIGN KEY("creator_id") REFERENCES "CREATOR"("creator_id") ON UPDATE CASCADE ON DELETE SET NULL,
	PRIMARY KEY("contract_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "RECORDING" (
	"song_id"	INTEGER,
	"singer_id"	INTEGER,
	FOREIGN KEY("song_id") REFERENCES "SONG"("song_id") ON UPDATE CASCADE ON DELETE SET NULL,
	FOREIGN KEY("singer_id") REFERENCES "CREATOR"("creator_id") ON UPDATE CASCADE ON DELETE SET NULL
);
CREATE TABLE IF NOT EXISTS "SALES" (
	"album_id"	INTEGER,
	"album_qtty"	INTEGER,
	"s_month"	varchar(20),
	"s_year"	INTEGER,
	PRIMARY KEY("s_month","s_year","album_id"),
	FOREIGN KEY("album_id") REFERENCES "ALBUM"("album_id") ON UPDATE CASCADE ON DELETE SET NULL
);
